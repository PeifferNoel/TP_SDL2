//Voici les différentes primitives associees a la fenetre SDL2

//SDL_DestroyWindow       -> Permet de détruire la fenêtre et libérer l'espace mémoire alloué
//SDL_Delay               -> Sert a mettre en pause le programme grace a un argument, le nombre de millisecondes pendant lequel le programme reste en pause
//SDL_SetWindowTitle      -> Changer le titre de la fenetre
//SDL_SetWindowIcon       -> Définir une icone de fenetre
//SDL_GetWindowTitle      -> Récupérer le titre de la fenetre
//SDL_SetWindowPosition   -> Definir la position de la fenetre
//SDL_SetWindowSize       -> Definir la taille de la fenetre
//SDL_GetWindowPosition   -> Récuperer la position de la fenetre
//SDL_GetWindowSize       -> Recuperer la taille de la fenetre
//SDL_MaximizeWindow      -> Agrandissement de la fenetre
//SDL_MinimizeWindow      -> Reduction de la fenetre
//SDL_RestoreWindow       -> Restaurer la fenetre
//SDL_SetWindowFullscreen -> Mettre la fenetre en plein ecran
//SDL_SetWindowBrightness -> Definir la luminosite de la fenetre
//SDL_GetWindowBrightness -> Recuperer la luminosite de la fenetre
